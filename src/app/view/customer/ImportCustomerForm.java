    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        String name = txtName.getText();
        String addr = txtAddress.getText();
        String phone = txtPhone.getText();
        boolean flag = true;
        if (name == "" || name.length() < 4 || name.length()>100) {
            lbName.setText("Name invalid, min: 5 chars, max: 100 chars");
            flag = false;
        }
        if (addr == "" || addr.length() < 6 || addr.length()>100) {
            lbAdd.setText("Address invalid, min 6 chars, max: 100 chars");
            flag = false;
        }
        if (phone == "" || phone.length() < 5 || phone.length()>20) {
            lbPhone.setText("Phone number invalid, min: 6 numbers, max: 20 chars. (not alphabet chars)");
            flag = false;
        }
        if (flag == true) {
            try {
                if (conn.isClosed()) {
                    txtError.setText("NOTE: Connection was closed.");

                }
                if (AppUtility.isExistAnObject("SELECT * FROM Customers WHERE name = N'" + name + "' and phone = '" + phone + "'", conn)) {
                    String sql = "insert into Customers values (N'" + name + "', N'" + addr + "', '" + phone + "')";
                    Statement stm = conn.createStatement();
                    int rs = stm.executeUpdate(sql);
                    lbOK.setText("Create customer: " + name + " successful.");
                    stm.close();
                } else {
                    txtError.setText("Existed Customer: " + name);
                }

            } catch (SQLException ex) {
                txtError.setText("Accur an error.");
                System.out.println("KQ: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnCreateActionPerformed

