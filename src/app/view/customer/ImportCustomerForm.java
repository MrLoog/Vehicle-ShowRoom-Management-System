    private void updateCustomerToDB(Customer c){
        try {
            CallableStatement cStmt = conn.prepareCall("{call updateCustomer(?, ?, ?, ?)}");
            cStmt.setString(1, c.getName());
            cStmt.setString(2, c.getAddress());
            cStmt.setString(3, c.getPhone());
            cStmt.setInt(4, c.getId());
            cStmt.executeUpdate();
            int id = cStmt.getInt(1);
            c.setId(id);
            customerModel.addCustomer(c);
            cStmt.close();
        } catch (SQLException ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "SQL Error");
        }
    }
