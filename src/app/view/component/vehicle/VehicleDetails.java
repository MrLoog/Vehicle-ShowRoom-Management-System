    public void setDataModel(Vehicle selectobj) {
        this.model = selectobj;
        jLabel2.setText(model.getModelNumber());
        jLabel4.setText(model.getName());
        jLabel6.setText(model.getBrand());
        jLabel8.setText(model.getPrice() + "");
        jLabel13.setText(model.getCategory());
        if (model.getQuantity() <= 0) {
            jLabel10.setText("Not Avaiable");
        } else {
            jLabel10.setText(model.getQuantity() + "");
            jComboBox1.removeAllItems();
            for (int i = 1; i <= model.getQuantity(); i++) {
                jComboBox1.addItem(i);
            }
            jComboBox1.setSelectedIndex(0);
            buyquantity = 1;
        }
    }
